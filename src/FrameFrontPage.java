    private void fileWriter() {
        try (java.io.BufferedWriter writer = new java.io.BufferedWriter(new java.io.FileWriter("antrian_kasir.txt"))) {
            for (Pesanan p : antrian.getSemua()) {
                String baris = p.getId() + "|" + p.getNama() + "|" + p.getPesananSebagaiString();
                writer.write(baris);
                writer.newLine();
            }
        } 
        catch (java.io.IOException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan ke file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

