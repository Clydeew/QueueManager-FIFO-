    private void btnTambahAntrianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahAntrianActionPerformed
        String id = txtIDPesanan.getText();
        String nama = txtNamaPelanggan.getText();
        String item = (String) boxPesanan.getSelectedItem();
        if(id.isEmpty() || nama.isBlank() || item.isBlank()){
            JOptionPane.showMessageDialog(this, "Isi seluruh data sebelum memasukkannya ke dalam antrian.", "Harap Ulangi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            Pesanan pesanan = new Pesanan(id, nama);
            pesanan.tambahPesanan(item);
            antrian.tambahPesanan(pesanan);
            add2Table();
            fileWriter();
            txtIDPesanan.setText("");
            txtNamaPelanggan.setText("");
        }
    }//GEN-LAST:event_btnTambahAntrianActionPerformed

    private void btnLayaniPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLayaniPesananActionPerformed
        if(antrian.antrianKosong()){
            labelNextInLine.setText("Kosong");
            JOptionPane.showMessageDialog(this, "Antrian Kosong", "Isi Antrian Terlebih Dahulu", JOptionPane.ERROR_MESSAGE);
        }
        Pesanan pesanan = antrian.layaniPesanan();
        labelNextInLine.setText("Sedang Melayani: " + pesanan.getNama());
        add2Table();
    }//GEN-LAST:event_btnLayaniPesananActionPerformed

