    public void add2Table(){
        List<Pesanan> data = antrian.getSemua();
        String[] coloumn = {"No. Antrian", "ID Pesanan", "Nama", "Pesanan"};
        DefaultTableModel model = new DefaultTableModel(coloumn, 0);

        int no = 1;
        for (Pesanan p : data) {
            model.addRow(new Object[]{
                no++,
                p.getId(),
                p.getNama(),
                p.getPesananSebagaiString()
            });
        }
        tabelAntrian.setModel(model);
    }
